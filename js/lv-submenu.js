/* Add JQuery animation to bootstrap dropdown elements.
*/

$(document).ready(function() {
    $("#js-drop1").click(function() {
        var isHidden = $("#menu1").is(":hidden");
        if (isHidden) {
            $("#menu1").slideDown();
        } else {
            $("#menu1").slideUp();
        }
    })
});

